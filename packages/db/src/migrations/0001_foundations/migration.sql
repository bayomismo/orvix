-- =============================================================================
-- 2. Tables
-- =============================================================================
--
-- The Prisma schema in `schema.prisma` is the source of truth at the
-- application layer. This migration is hand-written to:
--   - enable required Postgres extensions
--   - install row-level security
--   - harden the audit log against tampering
--   - implement the parent_of cycle-prevention trigger
--   - install the Merkle-style chain helper
--
-- Schema-level DDL is generated by Prisma migrate dev.

-- =============================================================================
-- 3. Row-Level Security
-- =============================================================================
--
-- Pattern: every tenant-scoped table enforces
--   `USING (workspace_id = current_setting('app.workspace_id')::uuid)`
--
-- `app.workspace_id` is set per request by the Prisma client extension
-- (see `packages/db/src/with-workspace.ts`). The application's DB role has
-- BYPASSRLS disabled. Even a DBA with elevated privilege is bounded by
-- the audit-log notarization in §6.

DO $$
DECLARE
  t text;
  tables text[] := ARRAY[
    'Workspace',
    'User',
    'Role',
    'Permission',
    'RolePermissionGrant',
    'Session',
    'BusinessDnaVersion',
    'WorkItemType',
    'WorkItem',
    'WorkItemCustomer',
    'WorkItemDeal',
    'WorkItemProject',
    'WorkItemTask',
    'WorkItemConversation',
    'WorkItemDocument',
    'WorkItemRequest',
    'Activity',
    'Comment',
    'CommentMention',
    'Attachment',
    'AttachmentReview',
    'WorkItemRelation',
    'AIAssistantConfig',
    'AIRoutingProfile',
    'AIRun',
    'AIMemoryEntry',
    'Automation',
    'AutomationRun',
    'AuditLog'
  ];
BEGIN
  FOREACH t IN ARRAY tables LOOP
    EXECUTE format('ALTER TABLE %I ENABLE ROW LEVEL SECURITY', t);
    -- NOTE: The USING-clause refers to Prisma-generated columns. If a
    -- column is renamed via Prisma, this migration must be updated.
    IF t <> 'Permission' THEN
      EXECUTE format(
        'CREATE POLICY %I ON %I FOR ALL TO PUBLIC USING (workspace_id = current_setting(''''app.workspace_id'''', true)::uuid) WITH CHECK (workspace_id = current_setting(''''app.workspace_id'''', true)::uuid)',
        t || '_tenant_isolation',
        t
      );
    END IF;
  END LOOP;
END
$$;

-- =============================================================================
-- 4. Audit log append-only enforcement
-- =============================================================================
--
-- v0.2 MUST: revoke UPDATE and DELETE on audit_logs from the application's
-- DB role. A separate role (or migration runner) retains DDL rights.

-- Create a dedicated app role and grant it the minimum needed. In a real
-- deployment these names + secrets come from environment variables; the
-- CREATE ROLE here is a guardrail that prevents the prisma role from
-- being used as a superuser.
DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = 'orvix_app') THEN
    CREATE ROLE orvix_app NOLOGIN;
  END IF;
END
$$;

