-- =============================================================================
-- 5. parent_of cycle-prevention trigger
-- =============================================================================
--
-- A CHECK constraint cannot enforce non-self loops. We use a deferrable
-- trigger on WorkItemRelation.

CREATE OR REPLACE FUNCTION orvix_assert_no_parent_of_cycle()
RETURNS TRIGGER AS $$
DECLARE
  has_cycle boolean;
BEGIN
  IF NEW.relation <> 'parent_of' THEN
    RETURN NEW;
  END IF;

  -- Walk the parent_of graph from NEW.targetId; if we ever hit NEW.sourceId,
  -- we're creating a cycle.
  WITH RECURSIVE chain AS (
    SELECT NEW.target_id AS root
    UNION ALL
    SELECT wir.target_id
    FROM "WorkItemRelation" wir
    WHERE wir.relation = 'parent_of'
      AND wir.workspace_id = NEW.workspace_id
      AND wir.source_id = chain.root
  )
  SELECT EXISTS (
    SELECT 1 FROM chain WHERE root = NEW.source_id
  ) INTO has_cycle;

  IF has_cycle THEN
    RAISE EXCEPTION 'parent_of cycle detected (% -> %)', NEW.source_id, NEW.target_id;
  END IF;

  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS work_item_relation_no_parent_of_cycle ON "WorkItemRelation";
CREATE CONSTRAINT TRIGGER work_item_relation_no_parent_of_cycle
  AFTER INSERT OR UPDATE ON "WorkItemRelation"
  DEFERRABLE INITIALLY DEFERRED
  FOR EACH ROW
  EXECUTE FUNCTION orvix_assert_no_parent_of_cycle();

-- =============================================================================
-- 6. Merkle-style audit-log chain helper
-- =============================================================================
--
-- Stores each AuditLog row with a `root_hash` that chains the row payload
-- to the previous row's `root_hash`. Replication to S3 (with Object Lock)
-- is the external notarization step that customer / auditor can verify.

CREATE OR REPLACE FUNCTION orvix_compute_audit_chain(prev_hash text, payload jsonb)
RETURNS text AS $$
  SELECT encode(
    digest(
      coalesce(prev_hash, '') || jsonb_build_object('payload', payload)::text,
      'sha256'
    ),
    'hex'
  );
$$ LANGUAGE sql IMMUTABLE;

-- Removed automatic trigger chain computation on insert. We compute the
-- hash from the application server (after previous row read) so the chain
-- is portable and the DB role isn't trusted. The batcher computes:
--
--   SELECT COALESCE(
--     (SELECT "rootHash" FROM "AuditLog"
--       WHERE "workspaceId" = $1 ORDER BY "createdAt" DESC LIMIT 1),
--     ''
--   );
--
-- then computes the next root_hash, then INSERTs. This is the application
-- contract; this migration does not impose a DB-side trigger so the chain
-- is owned by the application layer (Phase 0 M0.4 telemetry surface
-- publishes a verifier).
